n = int(input("Enter the no. of elements : "))
list1 = list()
for i in range (n) :
    ele = int(input("Enter any no. : "))
    list1.append(ele)
maximum = max(list1)
minimum = min(list1)
sum = sum(list1)
average = sum / len(list1)
print ('\nList is :', list1)
print ("Maximum no. in list is : ",maximum)
print ("Minimum no. in list is : ",minimum)
print ("Sum of elements in list is",sum)
print ("Average list is :",average)